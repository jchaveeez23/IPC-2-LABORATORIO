#Iteracion Con Ciclo While
n = 10
while n > 2:
    print(n)
    n -= 1
print('Ciclo Terminado!')

#Iteracion Con For Tipo Foreach
compras = ['Huevos','Leche','Pan']
for elemento in compras:
    print('Agregar al carro: ',elemento)
print('*Comprar*')

#Iteracion con For
for i in range(9):
    print('Iteracion No.',i)
print('Finalizado')